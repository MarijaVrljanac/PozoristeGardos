    private void btnDetaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetaljiActionPerformed
        int row = tblNalozi.getSelectedRow();
        ModelTabeleNalog mt = (ModelTabeleNalog) tblNalozi.getModel();
        Nalog nalog = mt.getSelectedNalog(row);
        if (row >= 0) {
            if(nalog.getNalogID() == n.getNalogID()){
                new NalogForm(this, rootPaneCheckingEnabled, n).setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this, "Nemate pristup ovim podacima!");
            }


        }
    }//GEN-LAST:event_btnDetaljiActionPerformed

